@pytest.mark.asyncio
async def test_numalgo_0(resolver):
    did = "did:peer:0z6MkqRYqQiSgvZQdnBytw86Qbs2ZWUkGv22od935YF4s8M7V"
    did_doc = await resolver.resolve(did)

    assert did_doc.did == did
    assert did_doc.authentication_kids == [
        "did:peer:0z6MkqRYqQiSgvZQdnBytw86Qbs2ZWUkGv22od935YF4s8M7V#6MkqRYqQiSgvZQdnBytw86Qbs2ZWUkGv22od935YF4s8M7V"]
    assert did_doc.key_agreement_kids == []
    assert did_doc.verification_methods == [
        VerificationMethod(
            id="did:peer:0z6MkqRYqQiSgvZQdnBytw86Qbs2ZWUkGv22od935YF4s8M7V#6MkqRYqQiSgvZQdnBytw86Qbs2ZWUkGv22od935YF4s8M7V",
            type=VerificationMethodType.JSON_WEB_KEY_2020,
            controller=did,
            verification_material=VerificationMaterial(
                format=VerificationMaterialFormat.JWK,
                value=json.dumps(
                    {
                        "kty": "OKP",
                        "crv": "Ed25519",
                        "x": "owBhCbktDjkfS6PdQddT0D3yjSitaSysP3YimJ_YgmA"
                    }
                )
            )
        )
    ]
    assert did_doc.didcomm_services == []


